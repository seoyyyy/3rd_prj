package kr.co.prj.vo;

public class MonthVO {
	private String roomNum, rsvYear, rsvMonth, rsvDay;

	public String getRoomNum() {
		return roomNum;
	}

	public void setRoomNum(String roomNum) {
		this.roomNum = roomNum;
	}

	public String getRsvYear() {
		return rsvYear;
	}

	public void setRsvYear(String rsvYear) {
		this.rsvYear = rsvYear;
	}

	public String getRsvMonth() {
		return rsvMonth;
	}

	public void setRsvMonth(String rsvMonth) {
		this.rsvMonth = rsvMonth;
	}

	public String getRsvDay() {
		return rsvDay;
	}

	public void setRsvDay(String rsvDay) {
		this.rsvDay = rsvDay;
	}
	
	
}
